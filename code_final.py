def visualize_all(user_data, user_name):
    save_dir = f"{user_name}_plots"
    os.makedirs(save_dir, exist_ok=True)

    # 1. Stress distribution
    if 'Stress_Val' in user_data['usercondition'].columns:
        plt.figure(figsize=(6,4))
        sns.countplot(data=user_data['usercondition'], x='Stress_Val', order=user_data['usercondition']['Stress_Val'].value_counts().index)
        plt.title(f"{user_name} Stress Distribution")
        plt.savefig(f"{save_dir}/stress_distribution.png", dpi=300)
        plt.show()

    # 2. Typing speed over time
    df = user_data['keystrokes'].copy()
    df['Press_Time'] = pd.to_datetime(df['Press_Time'], errors='coerce')
    df = df.dropna(subset=['Press_Time'])
    if len(df) > 1:
        df['inter_key'] = df['Press_Time'].diff().dt.total_seconds()
        plt.figure(figsize=(8,4))
        plt.plot(df['Press_Time'], df['inter_key'], marker='o', linestyle='-', markersize=3)
        plt.title(f"{user_name} Inter-Key Time Over Time")
        plt.xlabel("Time")
        plt.ylabel("Inter-Key Duration (s)")
        plt.savefig(f"{save_dir}/inter_key_time.png", dpi=300)
        plt.show()

    # 3. Mouse speed over time
    df_mouse = user_data['mouse_mov_speeds'].copy()
    df_mouse['Time'] = pd.to_datetime(df_mouse['Time'], errors='coerce')
    df_mouse = df_mouse.dropna(subset=['Time','Speed(ms)'])
    plt.figure(figsize=(8,4))
    plt.plot(df_mouse['Time'], df_mouse['Speed(ms)'], marker='o', linestyle='-', markersize=3)
    plt.title(f"{user_name} Mouse Speed Over Time")
    plt.xlabel("Time")
    plt.ylabel("Speed (ms)")
    plt.savefig(f"{save_dir}/mouse_speed.png", dpi=300)
    plt.show()

    # 4. Inactivity histogram
    df_inact = user_data['inactive']
    plt.figure(figsize=(6,4))
    sns.histplot(df_inact['Duration(s)'], bins=10, kde=True)
    plt.title(f"{user_name} Inactivity Duration Distribution")
    plt.xlabel("Duration (s)")
    plt.ylabel("Frequency")
    plt.savefig(f"{save_dir}/inactivity_histogram.png", dpi=300)
    plt.show()

    # 5. Active applications count
    df_app = user_data['activewindows']
    app_counts = df_app['App_Name'].value_counts()
    plt.figure(figsize=(8,4))
    sns.barplot(x=app_counts.index, y=app_counts.values)
    plt.xticks(rotation=45)
    plt.title(f"{user_name} Active Applications Frequency")
    plt.ylabel("Count")
    plt.xlabel("Application")
    plt.savefig(f"{save_dir}/active_applications.png", dpi=300)
    plt.show()

    # 6. Mouse movement distance over time
    df_mouse_data = user_data['mousedata'].copy()
    df_mouse_data['Time'] = pd.to_datetime(df_mouse_data['Time'], errors='coerce')
    df_mouse_data = df_mouse_data.sort_values('Time')
    df_mouse_data['dx'] = df_mouse_data['X'].diff()
    df_mouse_data['dy'] = df_mouse_data['Y'].diff()
    df_mouse_data['distance'] = np.sqrt(df_mouse_data['dx']**2 + df_mouse_data['dy']**2)
    df_mouse_data['cum_distance'] = df_mouse_data['distance'].cumsum()
    plt.figure(figsize=(8,4))
    plt.plot(df_mouse_data['Time'], df_mouse_data['cum_distance'])
    plt.title(f"{user_name} Cumulative Mouse Distance Over Time")
    plt.xlabel("Time")
    plt.ylabel("Distance (pixels)")
    plt.savefig(f"{save_dir}/cum_mouse_distance.png", dpi=300)
    plt.show()

# Run visualization
visualize_all(user1_data, "User 1")
