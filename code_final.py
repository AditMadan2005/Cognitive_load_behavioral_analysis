# 2. Feature extraction functions
# ----------------------------
def extract_keystroke_features(df):
    df['Press_Time'] = pd.to_datetime(df['Press_Time'], errors='coerce')
    df['Relase_Time'] = pd.to_datetime(df['Relase_Time'], errors='coerce')
    df = df.dropna(subset=['Press_Time','Relase_Time'])

    if len(df) > 0:
        df['key_duration'] = (df['Relase_Time'] - df['Press_Time']).dt.total_seconds()
        avg_duration = df['key_duration'].mean()
        df = df.sort_values('Press_Time')
        df['inter_key'] = df['Press_Time'].diff().dt.total_seconds()
        avg_inter_key = df['inter_key'].mean()
        typing_speed = len(df) / (df['Press_Time'].max() - df['Press_Time'].min()).total_seconds()
    else:
        avg_duration = avg_inter_key = typing_speed = np.nan
    return avg_duration, avg_inter_key, typing_speed

def extract_mouse_features(df):
    df['Time'] = pd.to_datetime(df['Time'], errors='coerce')
    df = df.dropna(subset=['Time','X','Y'])

    if len(df) > 0:
        df = df.sort_values('Time')
        df['dx'] = df['X'].diff()
        df['dy'] = df['Y'].diff()
        df['distance'] = np.sqrt(df['dx']**2 + df['dy']**2)
        df['time_diff'] = df['Time'].diff().dt.total_seconds()
        df['speed'] = df['distance'] / df['time_diff']
        avg_speed = df['speed'].mean()
        total_distance = df['distance'].sum()
    else:
        avg_speed = total_distance = np.nan
    return avg_speed, total_distance

def extract_inactivity_features(df):
    df['Stopped_Time'] = pd.to_datetime(df['Stopped_Time'], errors='coerce')
    df['Activated_Time'] = pd.to_datetime(df['Activated_Time'], errors='coerce')
    df = df.dropna(subset=['Stopped_Time','Activated_Time','Duration(s)'])

    if len(df) > 0:
        avg_inactivity = df['Duration(s)'].mean()
        max_inactivity = df['Duration(s)'].max()
        total_inactivity = df['Duration(s)'].sum()
    else:
        avg_inactivity = max_inactivity = total_inactivity = np.nan
    return avg_inactivity, max_inactivity, total_inactivity

def extract_stress_features(df):
    if 'Stress_Val' in df.columns:
        stress_mapping = {
            'Neutral': 0,
            'S_Stressed': 1,
            'V_Stressed': 2,
            'F_Good': 3,
            'F_Great': 4
        }
        df['stress_numeric'] = df['Stress_Val'].map(stress_mapping).fillna(0)
        avg_stress = df['stress_numeric'].mean()
    else:
        avg_stress = np.nan
    return avg_stress

def extract_all_features(user_data):
    kd, ik, ts = extract_keystroke_features(user_data['keystrokes'])
    ms, td = extract_mouse_features(user_data['mousedata'])
    avg_inactivity, max_inactivity, total_inactivity = extract_inactivity_features(user_data['inactive'])
    stress = extract_stress_features(user_data['usercondition'])

    return {
        'avg_key_duration': kd,
        'avg_inter_key': ik,
        'typing_speed': ts,
        'avg_mouse_speed': ms,
        'total_mouse_distance': td,
        'avg_inactivity': avg_inactivity,
        'max_inactivity': max_inactivity,
        'total_inactivity': total_inactivity,
        'avg_stress': stress
    }

